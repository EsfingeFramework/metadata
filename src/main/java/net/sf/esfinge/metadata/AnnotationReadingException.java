package net.sf.esfinge.metadata;

public class AnnotationReadingException extends Exception {

	public AnnotationReadingException() {
		super();
	}

	public AnnotationReadingException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public AnnotationReadingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AnnotationReadingException(String arg0) {
		super(arg0);
	}

	public AnnotationReadingException(Throwable arg0) {
		super(arg0);
	}

	
	
	
}
